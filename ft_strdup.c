#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tmp;
	int		len;
	size_t	i;

	i = 0;
	len = ft_strlen(s1);
	tmp = malloc(sizeof(char) * (len + 1));
	if (tmp == NULL)
		return (NULL);
	while (s1[i])
	{
		tmp[i] = s1[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
